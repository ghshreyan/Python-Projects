# Integer | immutable datatype

num1 = 100
print(type(num1))

# Float

num2 = 15.25
print(type(num2))

# String (Anything enclosed in " "" """ is interpreted as a string by Python) | Immutable datatype

num3 = "Testing quotes in 'Python' and triple quotes"
print(num3)
print(type(num3))

# List (Anything enlcosed in [] is a list) | Mutable datatype | Once updated add/update/delete is not possible

num4 = [10,2.5, 40,'Shreyan']

print(num4)
print(type(num4))

# Tuple | Immutable datatype | Once updated add/update/delete is not possible

num5 = (10,2.5,'Tuple')
print(num5)
print(type(num5))

# Dictionary | Mutable datatype

num5 = {"First_Name": "Shreyan", "Last_Name": "Ghosh"}
print(num5)
print(type(num5))

# Set | Mutable datatype

num6 = {'a',10, 2.5}
print(num6)
print(type(num6))

# Boolean | Only two datatypes without any quotes

True
False

# Complex | Format = Real + imaginary part

num7 = 3 + 4j
print(num7)
print(type(num7))
